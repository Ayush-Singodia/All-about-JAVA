public class forlooppr2 {
    public static void main(String args[]) {

        // Java program to print a star pattern
        // with the use of for loops
       
        for (int i = 1; i <= 4; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.print("*");
            }
            System.out.println();
        }

    }

}


